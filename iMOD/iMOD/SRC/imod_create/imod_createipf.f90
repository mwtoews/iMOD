 !###======================================================================
 SUBROUTINE CREATEIPF1MAIN(ITYPE,MESSAGE)
 !###======================================================================
 IMPLICIT NONE
 TYPE(WIN_MESSAGE),INTENT(IN) :: MESSAGE
 INTEGER,INTENT(IN)           :: ITYPE
 
 !## check polygon actions
 IACTSHAPES=(/1,3,3,3,3,3/)
 CALL POLYGON1MAIN(ITYPE,MESSAGE)
 IF(ITYPE.EQ.PUSHBUTTON.AND.MESSAGE%VALUE1.EQ.ID_ZOOMSELECT)THEN
  CALL IDFZOOM(ID_GOTOXY,0.0D0,0.0D0,0)
  CALL IDFPLOT(1)
 ENDIF

 CALL WDIALOGSELECT(MESSAGE%WIN)
 CALL WDIALOGFIELDSTATE(ID_INFO,MIN(1,SHP%NPOL))
 
 SELECT CASE(ITYPE)

  CASE (MENUSELECT)
   SELECT CASE (MESSAGE%VALUE1)
    !## current extent
    CASE (ID_FCUREXT)
     SHP%POL%IACT=0
     DO SHPI=1,SHP%NPOL
      IF(MPW%XMIN.LE.SHP%POL(SHPI)%X(1).AND.MPW%XMAX.GE.SHP%POL(SHPI)%X(1).AND. &
         MPW%YMIN.LE.SHP%POL(SHPI)%Y(1).AND.MPW%YMAX.GE.SHP%POL(SHPI)%Y(1))SHP%POL(SHPI)%IACT=1
     END DO
     !## entire domain
    CASE (ID_FENTDOM)
     SHP%POL%IACT=1
    CASE (ID_DESELECTALL)
     SHP%POL%IACT=0
    !## zoombox
    CASE (ID_FZOOMBOX)
     CALL CREATEIPF_SELECTBOX()
   END SELECT
   CALL WDIALOGSELECT(ID_DCREATEIPF)
   CALL WDIALOGPUTMENU(IDF_MENU1,SHP%POL%PNAME,SHP%NPOL,SHP%POL%IACT)
   CALL POLYGON1FIELDS(ID_DCREATEIPF) 

!  CASE(FIELDCHANGED)
 !  SELECT CASE (MESSAGE%VALUE2)
 !  END SELECT
  CASE(MOUSEBUTDOWN)
   SELECT CASE (MESSAGE%VALUE1)
    CASE (3)
     CALL WMENUFLOATING(ID_MENU10,MESSAGE%X,MESSAGE%Y)
   END SELECT
!  CASE(PUSHBUTTON)
!   SELECT CASE (MESSAGE%VALUE1)
!    CASE (ID_INFO)
!     !## read/show current data from memory!     
!     CALL POLYGON_UTL_FILLDATAGRID() 
!    CASE (IDCANCEL)
!     CALL CREATEIPF1CLOSE()
!    CASE (IDHELP)
!     CALL UTL_GETHELP('3.2.3','EMO.CreateIPF')
!   END SELECT

 END SELECT

 END SUBROUTINE

 !###======================================================================
 SUBROUTINE CREATEIPF_SELECTBOX()
 !###======================================================================
 IMPLICIT NONE
 INTEGER,PARAMETER :: MAXPOL=50
 INTEGER :: NPOL,ITYPE
 TYPE(WIN_MESSAGE) :: MESSAGE
 LOGICAL :: LEX
 REAL(KIND=DP_KIND),ALLOCATABLE,DIMENSION(:) :: XPOL,YPOL
 REAL(KIND=DP_KIND) :: XC1,YC1

 CALL WCURSORSHAPE(ID_CURSORPOLYGON)

 ALLOCATE(XPOL(MAXPOL),YPOL(MAXPOL)); NPOL=1

 CALL IGRPLOTMODE(MODEXOR)
 CALL IGRCOLOURN(WRGB(255,255,255))
 CALL IGRFILLPATTERN(OUTLINE)
 LEX=.FALSE.

 DO
  CALL WMESSAGE(ITYPE,MESSAGE)
  SELECT CASE (ITYPE)
   !## mouse-move
   CASE (MOUSEMOVE)
    CALL WINDOWSELECT(0)
    CALL WINDOWOUTSTATUSBAR(1,'Xcrd:'//TRIM(RTOS(REAL(MESSAGE%GX,8),'F',2))//' m, Ycrd:'// &
                                       TRIM(RTOS(REAL(MESSAGE%GY,8),'F',2))//' m')
    XC1=MESSAGE%GX; YC1=MESSAGE%GY

    IF(NPOL.GT.1)THEN
     CALL UTL_PLOT1BITMAP()
     IF(LEX)CALL UTL_PLOTPOLYGON(SIZE(XPOL),NPOL,XPOL,YPOL)
     LEX=.TRUE.
     XPOL(NPOL)=XC1; YPOL(NPOL)=YC1
     CALL UTL_PLOTPOLYGON(SIZE(XPOL),NPOL,XPOL,YPOL)
     CALL UTL_PLOT2BITMAP()
    ENDIF   

   CASE (MOUSEBUTDOWN)
    CALL UTL_PLOT1BITMAP()
    IF(LEX)CALL UTL_PLOTPOLYGON(SIZE(XPOL),NPOL,XPOL,YPOL)
    SELECT CASE (MESSAGE%VALUE1)
     !## left button
     CASE (1)
      XPOL(NPOL:NPOL+1)=XC1; YPOL(NPOL:NPOL+1)=YC1; NPOL=NPOL+1
      CALL UTL_PLOTPOLYGON(SIZE(XPOL),NPOL,XPOL,YPOL)
      CALL UTL_PLOT2BITMAP()
     !## right button
     CASE (3)
      NPOL=NPOL-1
      CALL UTL_PLOT2BITMAP()
      EXIT
    END SELECT
    !## bitmap scrolled, renew top-left pixel coordinates
   CASE (BITMAPSCROLLED)
    MPW%IX=MESSAGE%VALUE1; MPW%IY=MESSAGE%VALUE2
   END SELECT
 END DO

 SHP%POL%IACT=0
 IF(NPOL.GT.2)THEN
  DO SHPI=1,SHP%NPOL
   IF(MPW%XMIN.LE.SHP%POL(SHPI)%X(1).AND.MPW%XMAX.GE.SHP%POL(SHPI)%X(1).AND. &
      MPW%YMIN.LE.SHP%POL(SHPI)%Y(1).AND.MPW%YMAX.GE.SHP%POL(SHPI)%Y(1))THEN
    IF(DBL_IGRINSIDEPOLYGON(SHP%POL(SHPI)%X(1),SHP%POL(SHPI)%Y(1),XPOL,YPOL,NPOL+1).EQ.1)SHP%POL(SHPI)%IACT=1
   ENDIF
  END DO
 ENDIF
 DEALLOCATE(XPOL,YPOL)
 CALL WCURSORSHAPE(CURARROW)
 CALL IGRPLOTMODE(MODECOPY)
 CALL IGRFILLPATTERN(OUTLINE)

 END SUBROUTINE CREATEIPF_SELECTBOX

