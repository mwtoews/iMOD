 !###====================================================================
 SUBROUTINE LEG_SAMPLE_CLASSES(IPLOT,LTYPE,IOPTION,NLEG,XINT)
 !###====================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: IPLOT,IOPTION,NLEG,LTYPE
 REAL,INTENT(IN) :: XINT
 INTEGER :: I,J,K,M,N
 REAL :: DI,V1,V2,O1,O2,DC
 CHARACTER(LEN=50) :: TXT

 !## create smooth interval
 IF(IOPTION.EQ.0)THEN
  
  V1=MP(IPLOT)%LEG%CLASS(NLEG) !## min
  V2=MP(IPLOT)%LEG%CLASS(0)    !## max

  !## compute classes
  IF(LTYPE.EQ.1)THEN

   !## first quess interval
   M=MP(IPLOT)%LEG%NCLR
   N=M
   IF(V1-V2.NE.0.0)THEN
    CALL UTL_GETAXESCALES(V1,0.0,V2,1.0)
    !## number of classes
    M=NSX; DI=SXVALUE(2)-SXVALUE(1)

    !## increase legend to include all values
    IF(SXVALUE(1)  .GT.V1)THEN; SXVALUE(1)  =SXVALUE(1)  -DI; M=M+1; ENDIF
    IF(SXVALUE(NSX).LT.V2)THEN; SXVALUE(NSX)=SXVALUE(NSX)+DI; M=M+1; ENDIF
    !## top/bottom values of legend
    V1=SXVALUE(1); V2=SXVALUE(NSX)

    !## check i
    IF(M.GT.N)THEN
     DO WHILE(M.GT.N); M=M/2; DI=DI*2.0; ENDDO     
    ELSEIF(M.LT.N.AND.M.GT.0)THEN
     DO WHILE(M.LT.N); IF(M*2.GT.N)EXIT; M=M*2; DI=DI/2.0; ENDDO
    !## error occured, probably step too large
    ELSE
     CALL WMESSAGEBOX(OKONLY,EXCLAMATIONICON,COMMONOK,'iMOD cannot translate this legend into proper classes'//CHAR(13)// &
       'A reason for this might be the enormous legend range.','Error')
     M=MP(IPLOT)%LEG%NCLR 
     V1=MP(IPLOT)%LEG%CLASS(NLEG) !## min
     V2=MP(IPLOT)%LEG%CLASS(0)    !## max
     !## interval
     DI=(V2-V1)/REAL(M)
    ENDIF
   ELSE
    !## interval
    M=1; DI=(V2-V1)/REAL(M)
   ENDIF
  
  ELSE
   IF(V1-V2.NE.0.0)THEN
    M=(V2-V1)/XINT; DI=XINT
   ELSE
    M=1
   ENDIF   
  ENDIF

  !## look for appropriate start location for legend
  O1=MP(IPLOT)%LEG%CLASS(NLEG) !## min
  O2=MP(IPLOT)%LEG%CLASS(0)    !## max
  DO
   IF(V2-DI.LE.O2)EXIT
   V2=V2-DI
  ENDDO

  MP(IPLOT)%LEG%CLASS(0)=V2
  
  DO K=1,2
   IF(K.EQ.2)DC=REAL(NLEG)/REAL(I-1)

   I=0; J=0; DO 

    I=I+1; IF(I.GT.MXCLASS)EXIT
    V2=MP(IPLOT)%LEG%CLASS(0)-(REAL(I)*DI)

    MP(IPLOT)%LEG%CLASS(I)=V2 
    IF(K.EQ.2)THEN
     J=(REAL(I-1)*DC)+1
     J=MAX(1,MIN(J,NLEG))
     MP(IPLOT)%LEG%RGB(I)=MP(IPLOT)%LEG%RGB(J)
    ENDIF
    IF(V2.LT.O1)EXIT
   ENDDO
  END DO

  !## make sure take the last colour
  MP(IPLOT)%LEG%NCLR=MIN(MXCLASS,I)
 
 !## take over exactly
 ELSEIF(IOPTION.EQ.1)THEN

  DI=0.0
  DO I=1,MP(IPLOT)%LEG%NCLR 
   DI=DI+REAL(NLEG)/REAL(MP(IPLOT)%LEG%NCLR)
   J=INT(DI)
   J=MIN(J,NLEG) 
   MP(IPLOT)%LEG%CLASS(I)=MP(IPLOT)%LEG%CLASS(J)
   MP(IPLOT)%LEG%RGB(I)  =MP(IPLOT)%LEG%RGB(J)
  END DO
  MP(IPLOT)%LEG%CLASS(MP(IPLOT)%LEG%NCLR)=MP(IPLOT)%LEG%CLASS(NLEG)
  MP(IPLOT)%LEG%RGB(MP(IPLOT)%LEG%NCLR)  =MP(IPLOT)%LEG%RGB(NLEG)

 ENDIF
 
 DO I=1,MP(IPLOT)%LEG%NCLR 
  WRITE(TXT,'('//UTL_GETFORMAT(MP(IPLOT)%LEG%CLASS(I-1))//')') MP(IPLOT)%LEG%CLASS(I-1)
  MP(IPLOT)%LEG%LEGTXT(I)=ADJUSTL(TXT)
 ENDDO
 
 END SUBROUTINE LEG_SAMPLE_CLASSES

 !###====================================================================
 SUBROUTINE LEG_LEVELS(ID,IPLOT)
 !###====================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: ID,IPLOT
 INTEGER :: I,IROW,IR1,IG1,IB1,IR2,IG2,IB2
 REAL :: X1,X2
 CHARACTER(LEN=50) :: TXT

 CALL WDIALOGSELECT(ID_DLEGTAB1)
 CALL WDIALOGGETINTEGER(ID_IROW,IROW)
 IF(IROW.GT.MP(IPLOT)%LEG%NCLR)IROW=MP(IPLOT)%LEG%NCLR

 IF(ID.EQ.ID_PLUS)THEN

  !## interpolate new value
  CALL WGRIDGETCELLREAL(ID_GRIDLEVELS,1,IROW,X1)
  X2=X1-1.0
  IF(IROW-1.GE.1)CALL WGRIDGETCELLREAL(ID_GRIDLEVELS,1,IROW-1,X2)
  X1=(X1+X2)/2.0
  !## interpolate new colour
  CALL WGRIDGETCELLINTEGER(ID_GRIDLEVELS,3,IROW,I)
  CALL WRGBSPLIT(I,IR1,IG1,IB1)
  IF(IROW-1.GE.1)CALL WGRIDGETCELLINTEGER(ID_GRIDLEVELS,3,IROW-1,I)
  CALL WRGBSPLIT(I,IR2,IG2,IB2)
  IR1=INT((IR1+IR2)/2)
  IG1=INT((IG1+IG2)/2)
  IB1=INT((IB1+IB2)/2)
  I=WRGB(IR1,IG1,IB1)
  !## insert row
  CALL WGRIDINSERTROWS(ID_GRIDLEVELS,IROW,1,1,1)
  CALL WGRIDPUTCELLREAL(ID_GRIDLEVELS,1,IROW,X1) 
  IF(IROW.LT.MP(IPLOT)%LEG%NCLR)THEN
   CALL WGRIDGETCELLREAL(ID_GRIDLEVELS,1,IROW+1,X2)
   CALL WGRIDPUTCELLREAL(ID_GRIDLEVELS,2,IROW,X2) 
  ENDIF
  CALL WGRIDPUTCELLINTEGER(ID_GRIDLEVELS,3,IROW,I)

  WRITE(TXT,'('//UTL_GETFORMAT(X1)//')') X1
  CALL WGRIDPUTCELLSTRING(ID_GRIDLEVELS,4,IROW,TRIM(ADJUSTL(TXT)))

  MP(IPLOT)%LEG%NCLR=MIN(MXCLR,MP(IPLOT)%LEG%NCLR+1)

 ELSEIF(ID.EQ.ID_MIN)THEN

  CALL WGRIDDELETEROWS(ID_GRIDLEVELS,IROW,1,1,1)
  MP(IPLOT)%LEG%NCLR=MIN(MXCLR,MP(IPLOT)%LEG%NCLR-1)

 ENDIF

 CALL LEG_PLOT(IPLOT,0)

 END SUBROUTINE LEG_LEVELS

