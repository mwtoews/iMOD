   CALL WGRIDSETSELECTION(ID_GRIDLEVELS, 1,2,I,I)
  ELSE
   LEG_CHECK=.TRUE.
  ENDIF
 ELSE
  LEG_CHECK=.TRUE.
 ENDIF

