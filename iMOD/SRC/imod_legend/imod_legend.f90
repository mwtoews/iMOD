 !###======================================================================
 LOGICAL FUNCTION LEG_CREATE_CLASSES(LEGOPTION,LEGDOMAIN,JPLOT)
 !###======================================================================
 IMPLICIT NONE
 INTEGER,PARAMETER :: SAMPFACT=2000  !## inital number of samples
 INTEGER,INTENT(IN) :: JPLOT
 CHARACTER(LEN=3),INTENT(IN) :: LEGOPTION,LEGDOMAIN
 INTEGER :: N,I,J,ICOL,IROW,NODES,NUNIQUE,SAMPLE,LNCOL,LNROW,NC1,NC2,NR1,NR2,IRAT,IRAT1,IPLOT,IP,JP,IDR,IR,I1,I2
 REAL :: XMIN,XMAX,YMIN,YMAX,IDFVALUE,DMIN,DMAX,DR,X,DH,H,DX
 REAL,POINTER,DIMENSION(:) :: IDFVAL=>NULL()
 LOGICAL :: LTOOMUCH,LEX
 CHARACTER(LEN=50) :: LEGTXT,TXT1,TXT2
 CHARACTER(LEN=256) :: FNAME

 LEG_CREATE_CLASSES=.FALSE.
 
 IP=1; JP=MXMPLOT; IF(JPLOT.GT.0)THEN; ACTLIST(JPLOT)=1; IP=JPLOT; JP=IP; ENDIF
 
 DMIN=HUGE(1.0); DMAX=-HUGE(1.0); NODES=0

 DO IPLOT=IP,JP 
  IF(ACTLIST(IPLOT).NE.1)CYCLE

  IF(MP(IPLOT)%IPLOT.EQ.1.OR.MP(IPLOT)%IPLOT.EQ.5)THEN
   !## get idf for mdf file
   LEX=.TRUE.
   IF(MP(IPLOT)%IPLOT.EQ.5)THEN
    FNAME=MP(IPLOT)%IDFNAME
    !## read *.mdf file, only to get selected idf to be plotted
    IF(READMDF(MP(IPLOT)%IDFNAME,N))THEN
     MP(IPLOT)%IDFNAME=MDF(MP(IPLOT)%NLIDF)%FNAME
    ENDIF
   ENDIF
   IF(.NOT.LEX)RETURN
   IF(.NOT.IDFREAD(MP(IPLOT)%IDF,MP(IPLOT)%IDFNAME,0))RETURN
  ENDIF

  !## nonlinear/unique legend
  SELECT CASE (LEGOPTION)
   CASE ('NON','UQV')

    !## read extent of entire domain
    IF(LEGDOMAIN.EQ.'ALE')THEN
     LNCOL=MP(IPLOT)%IDF%NCOL; LNROW=MP(IPLOT)%IDF%NROW
     NC1=1; NC2=LNCOL; NR1=1; NR2=LNROW
    !## define extent of current domain (col*row)
    ELSEIF(LEGDOMAIN.EQ.'ALC')THEN
     XMIN=MAX(MP(IPLOT)%IDF%XMIN,MPW%XMIN); XMAX=MIN(MP(IPLOT)%IDF%XMAX,MPW%XMAX)
     YMIN=MAX(MP(IPLOT)%IDF%YMIN,MPW%YMIN); YMAX=MIN(MP(IPLOT)%IDF%YMAX,MPW%YMAX)
     CALL UTL_IDFCURDIM(XMIN,YMIN,XMAX,YMAX,MP(IPLOT)%IDF,NC1,NC2,NR1,NR2)
     LNCOL=NC2-NC1+1; LNROW=NR2-NR1+1
    ENDIF

    IF(LNCOL.GT.0.AND.LNROW.GT.0)THEN
    
     !## sample factor depends on number of cells (min. value =1)
     SAMPLE=NINT(SQRT(REAL(LNCOL*LNROW)/REAL(SAMPFACT))); SAMPLE=MAX(1,SAMPLE)

     !## initialize idfval2-array to max extent
     N=(LNCOL*LNROW)/SAMPLE**2
    
     !## increase memory
     CALL LEG_CREATE_ARRAYSIZE(IDFVAL,N,NODES)

     !## get idfvalue at sample-intervals, starting at distance 'sample' (nc1+sample-1,nr1+sample-1)
     DO IROW=(NR1+SAMPLE-1),NR2,SAMPLE
      DO ICOL=(NC1+SAMPLE-1),NC2,SAMPLE
       IDFVALUE=IDFGETVAL(MP(IPLOT)%IDF,IROW,ICOL,MP(IPLOT)%UNITS)
       IF(IDFVALUE.NE.MP(IPLOT)%IDF%NODATA)THEN
        NODES=NODES+1; IDFVAL(NODES)=IDFVALUE
       ENDIF
      END DO
     END DO
    ENDIF
    
    IF(LEGDOMAIN.EQ.'ALE')THEN
     DMIN=MIN(DMIN,MP(IPLOT)%IDF%DMIN); DMAX=MAX(DMAX,MP(IPLOT)%IDF%DMAX)
    ELSEIF(LEGDOMAIN.EQ.'ALC')THEN
     DMIN=MIN(DMIN,MP(IPLOT)%UMIN);     DMAX=MAX(DMAX,MP(IPLOT)%UMAX)
    ENDIF

   CASE ('LIN')
   
    IF(LEGDOMAIN.EQ.'ALE')THEN
     DMIN=MIN(DMIN,MP(IPLOT)%IDF%DMIN); DMAX=MAX(DMAX,MP(IPLOT)%IDF%DMAX)
    ELSEIF(LEGDOMAIN.EQ.'ALC')THEN
     DMIN=MIN(DMIN,MP(IPLOT)%UMIN);     DMAX=MAX(DMAX,MP(IPLOT)%UMAX)
    ENDIF

  END SELECT
  
  IF(MP(IPLOT)%IPLOT.EQ.1.OR.MP(IPLOT)%IPLOT.EQ.5)THEN
   CLOSE(MP(IPLOT)%IDF%IU); IF(MP(IPLOT)%IPLOT.EQ.5)MP(IPLOT)%IDFNAME=FNAME
  ENDIF

 ENDDO
 
 !## nonlinear legend - based upon percentiles
 SELECT CASE (LEGOPTION)
  CASE ('NON')

   IF(NODES.GT.0)THEN

    !## sort vector from small to large
    CALL WSORT(IDFVAL,1,NODES)

    !## remove duplicates
    J=1; DO I=2,NODES
     IF(UTL_EQUALS_REAL(IDFVAL(I),IDFVAL(J)))CYCLE
     J=J+1; IDFVAL(J)=IDFVAL(I)
    ENDDO

    !## add one artificial if only one value is found
    IF(J.EQ.1)THEN
     IDFVAL(1)=IDFVAL(1)-0.5
     IDFVAL(2)=IDFVAL(1)+1.0
     J=J+1
    ENDIF

    !## number of unique values
    NODES=J
    
    !## stepsize
    DX=REAL(NODES)/REAL(MXCLR) !-1)

    DO IPLOT=IP,JP 
     IF(ACTLIST(IPLOT).NE.1)CYCLE

     X=0.0-DX; DO I=MXCLR,0,-1
      X = X+DX
      I1= MAX(1,MIN(NODES,FLOOR(X))); I2=MAX(1,MIN(NODES,CEILING(X)))
      DH= IDFVAL(I2)-IDFVAL(I1)
      H =IDFVAL(I1)+(X-I1)*DH
      MP(IPLOT)%LEG%CLASS(I)=H
     END DO
     
     !## resample colours
     CALL LEG_SAMPLE_STRETCHED_GETIPOS(IPLOT)

     !## number of classes
     MP(IPLOT)%LEG%NCLR=MXCLR

    ENDDO

   ELSE

    MP(IPLOT)%LEG%CLASS(0)=MP(IPLOT)%IDF%DMAX
    MP(IPLOT)%LEG%CLASS(1)=MP(IPLOT)%IDF%DMIN
    MP(IPLOT)%LEG%NCLR=1
    MP(IPLOT)%LEG%RGB(1)=WRGB(25,25,25)
    CALL WMESSAGEBOX(OKONLY,INFORMATIONICON,COMMONOK,'iMOD found no values for percentiles!'//CHAR(13)// &
     'iMOD will set single class to minimal and maximal values ('//TRIM(RTOS(MP(IPLOT)%IDF%DMIN,'*',0))//'-'// &
      TRIM(RTOS(MP(IPLOT)%IDF%DMAX,'*',0))//')'//CHAR(13)//'according header in IDF','Information')

   ENDIF
 
  !## linear legend
  CASE ('LIN')

   DR=(DMAX-DMIN)/REAL(MXCLR+1)

   DO IPLOT=IP,JP 
    IF(ACTLIST(IPLOT).NE.1)CYCLE
    MP(IPLOT)%LEG%CLASS(0)=DMAX
    DO I=1,MXCLR; MP(IPLOT)%LEG%CLASS(I)=MP(IPLOT)%LEG%CLASS(I-1)-DR; ENDDO
    !## resample colours
    CALL LEG_SAMPLE_STRETCHED_GETIPOS(IPLOT)
    MP(IPLOT)%LEG%NCLR=MXCLR
   ENDDO
   
  !## unique values
  CASE ('UQV')

   IF(NODES.GT.0)THEN

    !## determine number of unique classes
    CALL UTL_GETUNIQUE_POINTER(IDFVAL,NODES,NUNIQUE)

    IF(NUNIQUE.GT.MXCLASS)THEN
     LTOOMUCH=.TRUE.
     CALL WMESSAGEBOX(OKONLY,EXCLAMATIONICON,COMMONOK,'iMOD found '//TRIM(ITOS(NUNIQUE))//' classes for selected domain'//&
       CHAR(13)//'The classes will be distributed evenly the occurence','Information')
     IRAT1=REAL(NUNIQUE)/REAL(MXCLASS)
     IRAT =1.0
     DO I=1,MXCLASS-1
      J        =INT(IRAT)
      IDFVAL(I)=IDFVAL(J)
      IRAT     =IRAT+IRAT1
     END DO
     IDFVAL(MXCLASS)=IDFVAL(NUNIQUE)
     NUNIQUE        =MXCLASS
    ELSE
     LTOOMUCH=.FALSE.
    ENDIF

    DO IPLOT=IP,JP 
     IF(ACTLIST(IPLOT).NE.1)CYCLE
     
     !## resample colours
     CALL LEG_SAMPLE_STRETCHED_GETIPOS(IPLOT)

     MP(IPLOT)%LEG%NCLR=MIN(MXCLASS,NUNIQUE)
     DO I=0,NUNIQUE
      IF(I.LT.NUNIQUE)THEN
       MP(IPLOT)%LEG%CLASS(I)=IDFVAL(I+1)
       IF(LTOOMUCH)THEN
        WRITE(MP(IPLOT)%LEG%LEGTXT(I+1),'('//UTL_GETFORMAT(IDFVAL(I+1))//')') IDFVAL(I+1)
        WRITE(LEGTXT,'('//UTL_GETFORMAT(IDFVAL(I+2))//')') IDFVAL(I+2)
        MP(IPLOT)%LEG%LEGTXT(I+1)=TRIM(MP(IPLOT)%LEG%LEGTXT(I+1))//'-'//TRIM(LEGTXT)
       ELSE
        WRITE(MP(IPLOT)%LEG%LEGTXT(I+1),'('//UTL_GETFORMAT(IDFVAL(I+1))//')') IDFVAL(I+1)
       ENDIF
       MP(IPLOT)%LEG%RGB(I+1)=ICOLOR(I+1)
      ELSE
       MP(IPLOT)%LEG%CLASS(I)=IDFVAL(I)-1.0
      ENDIF
     ENDDO
    ENDDO
    
   ELSE
    CALL WMESSAGEBOX(OKONLY,EXCLAMATIONICON,COMMONOK,'iMOD found no unique values!','Error')
   ENDIF

 END SELECT

 IF(ASSOCIATED(IDFVAL))DEALLOCATE(IDFVAL)

 SELECT CASE (LEGOPTION)
  CASE ('NON','LIN')
   DO IPLOT=IP,JP 
    IF(ACTLIST(IPLOT).NE.1)CYCLE
    !## generate legend-text for all
    DO I=1,MP(IPLOT)%LEG%NCLR
     WRITE(TXT1,'('//UTL_GETFORMAT(MP(IPLOT)%LEG%CLASS(I))//')')   MP(IPLOT)%LEG%CLASS(I)
     WRITE(TXT2,'('//UTL_GETFORMAT(MP(IPLOT)%LEG%CLASS(I-1))//')') MP(IPLOT)%LEG%CLASS(I-1)
     MP(IPLOT)%LEG%LEGTXT(I)=TRIM(ADJUSTL(TXT1)) 
    ENDDO
    CALL LEG_CREATE_COLORS(IPLOT)

   ENDDO
 END SELECT

 LEG_CREATE_CLASSES=.TRUE.

 END FUNCTION LEG_CREATE_CLASSES

 !###================================================================================
 SUBROUTINE LEG_CREATE_ARRAYSIZE(IDFVAL,N,NODES)
 !###================================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: N,NODES
 INTEGER :: M,I
 REAL,DIMENSION(:),POINTER,INTENT(INOUT) :: IDFVAL
 REAL,DIMENSION(:),POINTER :: IDFVAL_BU=>NULL()
 
 M=NODES+N
 IF(ASSOCIATED(IDFVAL))THEN
  IF(M.GT.SIZE(IDFVAL))THEN
   ALLOCATE(IDFVAL_BU(M)); DO I=1,NODES; IDFVAL_BU(I)=IDFVAL(I); ENDDO; DEALLOCATE(IDFVAL); IDFVAL=>IDFVAL_BU
  ENDIF
 ELSE
  ALLOCATE(IDFVAL(N))
 ENDIF

 END SUBROUTINE LEG_CREATE_ARRAYSIZE
 
 !##==============================================================================
 SUBROUTINE LEG_CREATE_COLORS(IPLOT)
 !##==============================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: IPLOT
 INTEGER :: I,J,K,IRED,IGREEN,IBLUE,I1,DC,DP
 REAL,DIMENSION(3) :: INTCLR
 INTEGER,DIMENSION(MXCGRAD,3) :: PCLR
 
 !## read current color settings on legend-dialog
 DO I=1,MXCGRAD
  CALL WRGBSPLIT(MP(IPLOT)%LEG%ICLRGRAD(I),PCLR(I,1),PCLR(I,2),PCLR(I,3))
 END DO

 CALL IGRPALETTEINIT()

 I1=1
 DO I=2,MXCGRAD
  IF(MP(IPLOT)%LEG%CGRAD(I).EQ.0)CYCLE
  !## get colour gradients
  DO J=1,3
   DC       = PCLR(I,J)-PCLR(I1,J)
   DP       =(CLRGIVEN(I)-CLRGIVEN(I1))+1
   INTCLR(J)= REAL(DC)/REAL(DP)
  END DO
  K=0
  DO J=CLRGIVEN(I1),CLRGIVEN(I)
   IRED=  PCLR(I1,1)+INTCLR(1)*REAL(K)
   IGREEN=PCLR(I1,2)+INTCLR(2)*REAL(K)
   IBLUE= PCLR(I1,3)+INTCLR(3)*REAL(K)
   MP(IPLOT)%LEG%RGB(J)=WRGB(IRED,IGREEN,IBLUE)
   K=K+1
  END DO
  I1=I
 END DO

 END SUBROUTINE LEG_CREATE_COLORS

