 SUBROUTINE ISGISP_MENUFIELDS(ICODE)
 !###======================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: ICODE
 INTEGER :: I,J

 CALL WINDOWSELECT(0)
 
 !## stop editing
 IF(ICODE.EQ.0)THEN
  I=1; J=0  
