 SUBROUTINE IDFTIMESERIES_RESIDUALS()
 !###======================================================================
 IMPLICIT NONE
 INTEGER :: I,II,J,K,N,NT,IROW,IYR,IMH,IDY,IHR,IMT,ISC
 INTEGER,ALLOCATABLE,DIMENSION(:) :: IT,TSIDATE
 INTEGER(KIND=8) :: PCKTIME_TS,PCKTIME_TSDIFF,PCKTIME_TSIPF
 REAL :: FRAC
 REAL,ALLOCATABLE,DIMENSION(:) :: XV
 REAL(KIND=8),ALLOCATABLE,DIMENSION(:) :: RTIME
 
 !## clean array
 IF(ALLOCATED(TSDIFF))THEN
  DO I=1,SIZE(TSDIFF)
   IF(ASSOCIATED(TSDIFF(I)%IDATE))DEALLOCATE(TSDIFF(I)%IDATE)
   IF(ASSOCIATED(TSDIFF(I)%VALUE))DEALLOCATE(TSDIFF(I)%VALUE)
  END DO
  DEALLOCATE(TSDIFF)
 ENDIF

 IROW=NIDF; IF(NIPF.GT.0)IROW=IROW+ASSF(1)%NCASS-1; IROW=IROW+1; LTYPE(IROW)%IAXES=1

 CALL WDIALOGSELECT(ID_DTIMESERIESTAB2)
 CALL WDIALOGPUTSTRING(IDF_STRING3,'Fluxes (m3/day)') 

 IF(IRESIDUAL.EQ.0)RETURN
 LTYPE(IROW)%IAXES=2
 
 CALL WDIALOGPUTSTRING(IDF_STRING3,'Difference') 
 
 !## Get all dates from IDF (and IPF) and sort them 
 N=SUM(NFILES)
 IF(NIPF.GT.0)THEN
  DO I=1,SIZE(TSIPF)
   N=N+SIZE(TSIPF(I)%IDATE)
  ENDDO
 ENDIF
 ALLOCATE(RTIME(N)); RTIME=0.0D0
 
 !## IDF
 K=0
 DO J=1,NIDF
  DO I=1,NFILES(J)   
   IYR=TS(J)%IYR(I); IMH=TS(J)%IMH(I)
   IDY=TS(J)%IDY(I); IHR=TS(J)%IHR(I)
   IMT=TS(J)%IMT(I); ISC=TS(J)%ISC(I)   
   K=K+1
   RTIME(K)=IYR*10000000000+IMH*100000000+IDY*1000000+IHR*10000+IMT*100+ISC
  ENDDO
 ENDDO
 
 !## IPF
 IF(NIPF.GT.0)THEN
  DO J=1,SIZE(TSIPF)
   DO I=1,SIZE(TSIPF(J)%IDATE)   
    IYR=TSIPF(J)%IYR(I); IMH=TSIPF(J)%IMH(I)
    IDY=TSIPF(J)%IDY(I); IHR=TSIPF(J)%IHR(I)
    IMT=TSIPF(J)%IMT(I); ISC=TSIPF(J)%ISC(I)   
    K=K+1
    RTIME(K)=IYR*10000000000+IMH*100000000+IDY*1000000+IHR*10000+IMT*100+ISC
   ENDDO
  ENDDO 
 ENDIF

 CALL WSORT(RTIME,1,K)
 
 !## Check RTIME on doubles and count number of unique values
 DO II=1,2
  N=1
  DO I=2,SIZE(RTIME)
   IF(ABS(RTIME(I)-RTIME(I-1)).GT.0.5)THEN
    N=N+1
    IF(II.EQ.2)THEN
     CALL ITIMETOGDATE(INT8(RTIME(I)),TSDIFF(1)%IYR(N),TSDIFF(1)%IMH(N),TSDIFF(1)%IDY(N),TSDIFF(1)%IHR(N),TSDIFF(1)%IMT(N),TSDIFF(1)%ISC(N))
     TSDIFF(1)%IDATE(N)=JD(TSDIFF(1)%IYR(N),TSDIFF(1)%IMH(N),TSDIFF(1)%IDY(N))
     FRAC=(TSDIFF(1)%IHR(N)*24*60*60+TSDIFF(1)%IMT(N)*60+TSDIFF(1)%ISC(N))/86400.0
     TSDIFF(1)%IDATE(N)=TSDIFF(1)%IDATE(N)+FRAC

    ENDIF
   ENDIF
  ENDDO
  IF(II.EQ.1)THEN
   ALLOCATE(TSDIFF(1))
   ALLOCATE(TSDIFF(1)%IDATE(N))
   ALLOCATE(TSDIFF(1)%VALUE(N))
   ALLOCATE(TSDIFF(1)%IYR(N))
   ALLOCATE(TSDIFF(1)%IMH(N))
   ALLOCATE(TSDIFF(1)%IDY(N))
   ALLOCATE(TSDIFF(1)%IHR(N))
   ALLOCATE(TSDIFF(1)%IMT(N))
   ALLOCATE(TSDIFF(1)%ISC(N))
   TSDIFF(1)%NODATA=-999.99
   CALL ITIMETOGDATE(INT8(RTIME(1)),TSDIFF(1)%IYR(1),TSDIFF(1)%IMH(1),TSDIFF(1)%IDY(1),TSDIFF(1)%IHR(1),TSDIFF(1)%IMT(1),TSDIFF(1)%ISC(1))
   TSDIFF(1)%IDATE(1)=JD(TSDIFF(1)%IYR(1),TSDIFF(1)%IMH(1),TSDIFF(1)%IDY(1))
  ENDIF
 ENDDO
 
 DEALLOCATE(RTIME)
 
 ALLOCATE(TSIDATE(SIZE(TSDIFF(1)%IDATE)))
 
 DO I=2,SIZE(TSDIFF(1)%IDATE)
  TSIDATE(I)=TSDIFF(1)%IDATE(I)-TSDIFF(1)%IDATE(I-1)
 ENDDO
 TSIDATE(1)=0.0D0
 TSDIFF(1)%IDATE(1)=0.0D0
 DO I=2,SIZE(TSDIFF(1)%IDATE)
  TSDIFF(1)%IDATE(I)=TSDIFF(1)%IDATE(I-1)+TSIDATE(I)
 ENDDO

 ALLOCATE(IT(2),XV(2))

 !## initialise
 IT=1
 TSDIFF(1)%VALUE=TSDIFF(1)%NODATA

 !## change it for ipf's
 J=NIDF
 IF(NIPF.GT.0)THEN
  PCKTIME_TSDIFF=TSDIFF(1)%IYR(1)*10000000000+TSDIFF(1)%IMH(1)*100000000+TSDIFF(1)%IDY(1)*1000000+TSDIFF(1)%IHR(1)*10000+TSDIFF(1)%IMT(1)*100+TSDIFF(1)%ISC(1)   
  DO K=1,SIZE(TSIPF)
   J=J+1
   DO I=1,SIZE(TSIPF(K)%IDATE)
    PCKTIME_TSIPF = TSIPF(1)%IYR(I)*10000000000+TSIPF(1)%IMH(I)*100000000+TSIPF(1)%IDY(I)*1000000+TSIPF(1)%IHR(I)*10000+TSIPF(1)%IMT(I)*100+TSIPF(1)%ISC(I)   
    IF(PCKTIME_TSIPF.GE.PCKTIME_TSDIFF)THEN
     IT(J)=I
     EXIT
    ENDIF
   END DO
  ENDDO
 ENDIF

 !## compute difference
 DO I=1,N
  PCKTIME_TSDIFF=TSDIFF(1)%IYR(I)*10000000000+TSDIFF(1)%IMH(I)*100000000+TSDIFF(1)%IDY(I)*1000000+TSDIFF(1)%IHR(I)*10000+TSDIFF(1)%IMT(I)*100+TSDIFF(1)%ISC(I)   
  
  NT=0
  J =0
  !## get idf value
  DO K=1,NIDF
   J=J+1
   !## package time
   PCKTIME_TS=TS(K)%IYR(IT(J))*10000000000+TS(K)%IMH(IT(J))*100000000+TS(K)%IDY(IT(J))*1000000+TS(K)%IHR(IT(J))*10000+TS(K)%IMT(IT(J))*100+TS(K)%ISC(IT(J))   
    IF(PCKTIME_TS.EQ.PCKTIME_TSDIFF)THEN
     NT    =NT+1
     XV(NT)=TS(K)%VALUE(IT(J))
     IT(J) =MIN(SIZE(TS(K)%VALUE),IT(J)+1)
    ENDIF
  END DO

  !## get ipf value
  IF(NIPF.GT.0)THEN
   !## check first column of associated file only!
   DO K=1,1
    J=J+1
    PCKTIME_TSIPF = TSIPF(1)%IYR(IT(J))*10000000000+TSIPF(1)%IMH(IT(J))*100000000+TSIPF(1)%IDY(IT(J))*1000000+TSIPF(1)%IHR(IT(J))*10000+TSIPF(1)%IMT(IT(J))*100+TSIPF(1)%ISC(IT(J))   
    IF(PCKTIME_TSIPF.EQ.PCKTIME_TSDIFF)THEN
      NT   =NT+1
      XV(NT)=TSIPF(K)%VALUE(IT(J))
      IT(J)=IT(J)+1
     ENDIF
   END DO
  ENDIF
  
  !## exist both, ready to compute difference
  IF(NT.EQ.2)THEN
   TSDIFF(1)%VALUE(I)=XV(1)-XV(2)
  ENDIF

 ENDDO

 DEALLOCATE(IT,XV)

 END SUBROUTINE IDFTIMESERIES_RESIDUALS

