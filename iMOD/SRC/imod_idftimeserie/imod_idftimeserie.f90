 !###======================================================================
 SUBROUTINE IDFTIMESERIE_IPF()
 !###======================================================================
 IMPLICIT NONE
 CHARACTER(LEN=256) :: FNAME
 INTEGER :: I,J
 INTEGER :: IYR,IMH,IDY

 IF(NIPF.EQ.0)RETURN
 !## nothing selected yet
 IF(JIPF.LE.0)RETURN

 !## no associated file with ipf
 IF(IPF(JIPF)%ACOL.EQ.0)RETURN

 !## find directory
 I    =INDEXNOCASE(IPF(JIPF)%FNAME,'\',.TRUE.)
 FNAME=IPF(JIPF)%FNAME(1:I-1)
 FNAME=TRIM(FNAME)//'\'//TRIM(IPF(JIPF)%INFO(IPF(JIPF)%ACOL,ISEL))//'.'//TRIM(ADJUSTL(IPF(JIPF)%FEXT))

 !## read dimensions of associated file and read the associated file
 CALL IPFDIMENSIONASSFILE(1,FNAME,IPF(JIPF)%IAXES)
 IF(ASSF(1)%ITOPIC.NE.1)THEN
  CALL WMESSAGEBOX(OKONLY,EXCLAMATIONICON,COMMONOK,'You cannot select an IPF file for the TimeSeries Tool'//CHAR(13)// &
     'That does not contain timeseries'//CHAR(13)// &
     'The file has been excluded for the rest of this session','Error')
  !## turn if off for the rest to avoid repetition of this message
  IPF(JIPF)%ACOL=0
  RETURN
 ENDIF
 ASSF(1)%ASSCOL1=IPF(JIPF)%ASSCOL1 !## column used with dlf
 ASSF(1)%ASSCOL2=IPF(JIPF)%ASSCOL2 !## on default not used --- border rings

 !## make copy from assf() into tsipf()  --- for duration purposes
 IF(ALLOCATED(TSIPF))THEN
  DO I=1,SIZE(TSIPF)
   IF(ASSOCIATED(TSIPF(I)%IDATE))DEALLOCATE(TSIPF(I)%IDATE)
   IF(ASSOCIATED(TSIPF(I)%VALUE))DEALLOCATE(TSIPF(I)%VALUE)
   IF(ASSOCIATED(TSIPF(I)%IYR))DEALLOCATE(TSIPF(I)%IYR)
   IF(ASSOCIATED(TSIPF(I)%IMH))DEALLOCATE(TSIPF(I)%IMH)
   IF(ASSOCIATED(TSIPF(I)%IDY))DEALLOCATE(TSIPF(I)%IDY)
   IF(ASSOCIATED(TSIPF(I)%IHR))DEALLOCATE(TSIPF(I)%IHR)
   IF(ASSOCIATED(TSIPF(I)%IMT))DEALLOCATE(TSIPF(I)%IMT)
   IF(ASSOCIATED(TSIPF(I)%ISC))DEALLOCATE(TSIPF(I)%ISC)   
  END DO
  DEALLOCATE(TSIPF)
 ENDIF
 ALLOCATE(TSIPF(ASSF(1)%NCASS-1))
 DO J=1,ASSF(1)%NCASS-1
  ALLOCATE(TSIPF(J)%IDATE(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%VALUE(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%IYR(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%IMH(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%IDY(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%IHR(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%IMT(ASSF(1)%NRASS))
  ALLOCATE(TSIPF(J)%ISC(ASSF(1)%NRASS))
 END DO

 DO I=1,ASSF(1)%NRASS
  DO J=1,ASSF(1)%NCASS-1
   TSIPF(J)%IDATE(I)=ASSF(1)%IDATE(I)-MINDATE
   TSIPF(J)%VALUE(I)=ASSF(1)%MEASURE(J,I)
   ASSF(1)%IDATE(I)=UTL_JDATETOIDATE(INT(ASSF(1)%IDATE(I)))
   CALL IDATETOGDATE(INT(ASSF(1)%IDATE(I)),IYR,IMH,IDY)
   TSIPF(J)%IYR(I)=IYR
   TSIPF(J)%IMH(I)=IMH
   TSIPF(J)%IDY(I)=IDY
   TSIPF(J)%IHR(I)=0
   TSIPF(J)%IMT(I)=0
   TSIPF(J)%ISC(I)=0
  ENDDO
 ENDDO

 END SUBROUTINE IDFTIMESERIE_IPF

