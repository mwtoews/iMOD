 CALL WDIALOGGETINTEGER(IDF_INTEGER6,TYR)
 CALL WDIALOGGETINTEGER(IDF_INTEGER3,FDY)
 CALL WDIALOGGETINTEGER(IDF_INTEGER5,TDY)
