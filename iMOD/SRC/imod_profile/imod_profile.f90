 !###======================================================================
 SUBROUTINE PROFILE_BITMAPIDF(IWINID)
 !###======================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: IWINID
 INTEGER :: IIDF,I,J,ICLR
 LOGICAL :: LEX
 REAL(KIND=DP_KIND) :: RADX,Y,Z,Z1,Z2,X1,X2,X3,X4,Y1,Y2,Y3,Y4,XX1,XX2,YY1,YY2

 IF(MXNIDF.LE.0)RETURN

 CALL IGRLINETYPE(SOLIDLINE)
 CALL IGRFILLPATTERN(SOLID)
 CALL IGRLINEWIDTH(LINETHICKNESS)
 
 RADX=(XMAX-XMIN)/500.0D0
 
 DO IIDF=1,MXNIDF

  CALL UTL_FILLARRAY(IPRF,7,PROFIDF(IIDF)%PRFTYPE)
  IF(IPRF(1).EQ.0.OR. &
    (IWINID.NE.PROFIDF(IIDF)%ISCREEN.AND.PROFIDF(IIDF)%ISCREEN.NE.0))CYCLE
  
  !## normal cross-section
  IF(PROFIDF(IIDF)%IDF%ITB.EQ.0)THEN

   !## overrule (temporary) profile-settings for colouring
   IPRF(5)=MAX(0,IPRF(5),IPRF(6))
   !## correct whenever coloring is done for the first or last one
   IF(IIDF.EQ.1.OR.IIDF.EQ.MXNIDF)THEN
    IPRF(5)=0; IPRF(6)=0
   ENDIF

   !## colouring
   IF(IPRF(5).EQ.1)THEN
    
    !## lines
    IF(IBLOCKFILLS.EQ.0)THEN

     DO I=2,SERIE(IIDF)%N 

      IF(SERIE(IIDF-1)%Y(I-1).NE.PROFIDF(IIDF-1)%IDF%NODATA.AND. &
         SERIE(IIDF-1)%Y(I)  .NE.PROFIDF(IIDF-1)%IDF%NODATA.AND. &
         SERIE(IIDF+1)%Y(I-1).NE.PROFIDF(IIDF+1)%IDF%NODATA.AND. &
         SERIE(IIDF+1)%Y(I)  .NE.PROFIDF(IIDF+1)%IDF%NODATA.AND. &
        (SERIE(IIDF  )%Y(I-1).NE.PROFIDF(IIDF)%IDF%NODATA.OR. &
         SERIE(IIDF  )%Y(I)  .NE.PROFIDF(IIDF)%IDF%NODATA))THEN

       X1=SERIE(IIDF-1)%X(I-1)
       X2=SERIE(IIDF-1)%X(I  )
       X3=SERIE(IIDF+1)%X(I  )
       X4=SERIE(IIDF+1)%X(I-1)

       Y1=SERIE(IIDF-1)%Y(I-1)
       Y2=SERIE(IIDF-1)%Y(I  )
       Y3=SERIE(IIDF+1)%Y(I  )
       Y4=SERIE(IIDF+1)%Y(I-1)

       Z1=SERIE(IIDF)%Y(I-1)
       Z2=SERIE(IIDF)%Y(I  )

       IF(Z1.NE.PROFIDF(IIDF)%IDF%NODATA.AND.Z2.NE.PROFIDF(IIDF)%IDF%NODATA)THEN
        !## split
        IF(Z1.NE.Z2)THEN
         YY1=0.5*(Y1+Y2)
         YY2=0.5*(Y3+Y4)
         XX1=0.5*(X1+X2)
         XX2=0.5*(X3+X4)
         !## devide by thickness if iprf(6).eq.1
         Z=Z1
         Y=PROFILE_BITMAPIDF_CLR_Y(Z1,Z,Z2,IPRF(6))
         !## get colour of serie()%y()
         ICLR=UTL_IDFGETCLASS(PROFIDF(IIDF)%LEG,Y); CALL IGRCOLOURN(ICLR)
         CALL DBL_IGRPOLYGONCOMPLEX((/X1,XX2,XX2,X4/),(/Y1,YY1,YY2,Y4/),4)
         Z =Z2
         X1=XX1
         X4=XX1
         Y1=YY1
         Y4=YY2
        ELSE
         Z =Z1
        ENDIF               
       ELSEIF(Z1.EQ.PROFIDF(IIDF)%IDF%NODATA)THEN
        X1=X1+(X2-X1)/2.0
        X4=X4+(X4-X3)/2.0
        Z =Z2
       ELSE
        X2=X2-(X2-X1)/2.0
        X3=X3-(X3-X4)/2.0
        Z =Z1
       ENDIF

       !## devide by thickness if iprf(6).eq.1
       Y=PROFILE_BITMAPIDF_CLR_Y(Z1,Z,Z2,IPRF(6))
       !## get colour of serie()%y()
       ICLR=UTL_IDFGETCLASS(PROFIDF(IIDF)%LEG,Y); CALL IGRCOLOURN(ICLR)
       CALL DBL_IGRPOLYGONCOMPLEX((/X1,X2,X3,X4/),(/Y1,Y2,Y3,Y4/),4)
!        CALL DBL_IGRPOLYGONCOMPLEX((/SERIE(IIDF-1)%X(I-1),SERIE(IIDF-1)%X(I),SERIE(IIDF+1)%X(I),SERIE(IIDF+1)%X(I-1)/), &
!                               (/SERIE(IIDF-1)%Y(I-1),SERIE(IIDF-1)%Y(I),SERIE(IIDF+1)%Y(I),SERIE(IIDF+1)%Y(I-1)/),4)

      ENDIF
     ENDDO
    
    !## blockfills
    ELSE

     DO I=1,SERIE(IIDF)%N     
      IF(SERIE(IIDF-1)%Y(I).NE.PROFIDF(IIDF-1)%IDF%NODATA.AND. &
         SERIE(IIDF+1)%Y(I).NE.PROFIDF(IIDF+1)%IDF%NODATA.AND. &
         SERIE(IIDF  )%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN

       Y=PROFILE_BITMAPIDF_CLR_Y(SERIE(IIDF-1)%Y(I),SERIE(IIDF)%Y(I),SERIE(IIDF+1)%Y(I),IPRF(6))
       !## get colour of serie()%y()
       ICLR=UTL_IDFGETCLASS(PROFIDF(IIDF)%LEG,Y); CALL IGRCOLOURN(ICLR)
       
       !## skip equal dy
       IF(SERIE(IIDF-1)%Y(I).NE.SERIE(IIDF+1)%Y(I))THEN
            
        !## start rectangle
        X1=SERIE(IIDF-1)%X(I)
        IF(I.GT.1)THEN
         IF(SERIE(IIDF-1)%Y(I-1).NE.PROFIDF(IIDF-1)%IDF%NODATA)THEN
          X1=(SERIE(IIDF-1)%X(I-1)+SERIE(IIDF-1)%X(I))/2.0
         ENDIF
        ENDIF
        !## end rectangle
        X2=SERIE(IIDF-1)%X(I)
        IF(I.LT.SERIE(IIDF)%N)THEN   
         IF(SERIE(IIDF-1)%Y(I+1).NE.PROFIDF(IIDF-1)%IDF%NODATA)THEN
          X2=(SERIE(IIDF-1)%X(I)+SERIE(IIDF-1)%X(I+1))/2.0
         ENDIF
        ENDIF     

        IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,SERIE(IIDF-1)%Y(I),X2,SERIE(IIDF+1)%Y(I))
       
       ENDIF
       
      ELSE
       J=0
      ENDIF
     ENDDO
    
    ENDIF
   ENDIF 
  
  !## filled cross-section
  ELSEIF(PROFIDF(IIDF)%IDF%ITB.EQ.1)THEN

   !## skip equal top/bot
   IF(PROFIDF(IIDF)%IDF%TOP.NE.PROFIDF(IIDF)%IDF%BOT)THEN

    !## obsolute
    DO I=1,SERIE(IIDF)%N 
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      ICLR=UTL_IDFGETCLASS(PROFIDF(IIDF)%LEG,SERIE(IIDF)%Y(I))
      !## skip white-colour
      IF(ICLR.EQ.WRGB(255,255,255))CYCLE
      CALL IGRCOLOURN(ICLR)
      IF(I.EQ.1)THEN
       X1= SERIE(IIDF)%X(I)
       X2=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I+1))/2.0
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,PROFIDF(IIDF)%IDF%BOT,X2,PROFIDF(IIDF)%IDF%TOP)   
      ELSEIF(I.EQ.SERIE(IIDF)%N)THEN 
       X1=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0
       X2= SERIE(IIDF)%X(I)
       IF(SERIE(IIDF)%X(I).EQ.SERIE(IIDF)%X(I-1))SERIE(IIDF)%X(I-1)=SERIE(IIDF)%X(I-1)-1
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,PROFIDF(IIDF)%IDF%BOT,X2,PROFIDF(IIDF)%IDF%TOP)   
      ELSE
       X1=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0
       X2=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I+1))/2.0
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,PROFIDF(IIDF)%IDF%BOT,X2,PROFIDF(IIDF)%IDF%TOP)
      ENDIF
     ENDIF
    ENDDO
   
   ENDIF
   
  ENDIF
 
 ENDDO

 DO IIDF=1,MXNIDF

  CALL UTL_FILLARRAY(IPRF,7,PROFIDF(IIDF)%PRFTYPE)
  IF(IPRF(1).EQ.0.OR. &
    (IWINID.NE.PROFIDF(IIDF)%ISCREEN.AND.PROFIDF(IIDF)%ISCREEN.NE.0))CYCLE

  !## normal cross-section
  IF(PROFIDF(IIDF)%IDF%ITB.NE.0)CYCLE

  CALL IGRCOLOURN(PROFIDF(IIDF)%SCOLOR)

  !## lines
  IF(IPRF(2).EQ.1)THEN
   !## lines from centre
   IF(IBLOCKLINES.EQ.0)THEN
    LEX=.TRUE.
    DO I=1,SERIE(IIDF)%N
     IF(SERIE(IIDF)%Y(I).EQ.PROFIDF(IIDF)%IDF%NODATA)LEX=.TRUE.
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      IF(LEX)THEN
       CALL DBL_IGRMOVETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I)); LEX=.FALSE.
      ELSE
       CALL DBL_IGRLINETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I))
      ENDIF
     ENDIF
    ENDDO
   !## blocklines
   ELSE
    LEX=.TRUE.; J=0
    DO I=1,SERIE(IIDF)%N 
     IF(SERIE(IIDF)%Y(I).EQ.PROFIDF(IIDF)%IDF%NODATA)LEX=.TRUE.
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      J=J+1
      IF(LEX)THEN
       CALL DBL_IGRMOVETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I)); LEX=.FALSE.
      ELSE
       CALL DBL_IGRLINETO((SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0,SERIE(IIDF)%Y(I-1))
       CALL DBL_IGRLINETO((SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0,SERIE(IIDF)%Y(I))
      ENDIF
     ELSE
      IF(J.GT.1)CALL DBL_IGRLINETO(SERIE(IIDF)%X(I-1),SERIE(IIDF)%Y(I-1)); J=0
     ENDIF
    ENDDO
   ENDIF

  ENDIF
  
  !## dots
  IF(IPRF(3).EQ.1)THEN
   DO I=1,SERIE(IIDF)%N
    IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)CALL DBL_IGRCIRCLE(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I),RADX)
   ENDDO
  ENDIF

  !## fills
  IF(IPRF(4).EQ.1)THEN

   IF(IBLOCKFILLS.EQ.0)THEN
    J=0
    DO I=1,SERIE(IIDF)%N 
     IF(SERIE(IIDF)%Y(I).EQ.PROFIDF(IIDF)%IDF%NODATA)J=0
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      J=J+1
      IF(J.GE.2)THEN
       CALL DBL_IGRPOLYGONCOMPLEX((/SERIE(IIDF)%X(I-1),SERIE(IIDF)%X(I),SERIE(IIDF)%X(I),SERIE(IIDF)%X(I-1)/), &
                              (/SERIE(IIDF)%Y(I-1),SERIE(IIDF)%Y(I),YMIN            ,YMIN/),4)
      ENDIF
     ENDIF
    ENDDO
   
   ELSE
    CALL UTL_DEBUGLEVEL(0)
    DO I=1,SERIE(IIDF)%N 
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      IF(I.EQ.1)THEN
       X1= SERIE(IIDF)%X(I)
       X2=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I+1))/2.0
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,YMIN,X2,SERIE(IIDF)%Y(I))   
      ELSEIF(I.EQ.SERIE(IIDF)%N)THEN 
       X1=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0
       X2= SERIE(IIDF)%X(I)
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,YMIN,X2,SERIE(IIDF)%Y(I))   
      ELSE
       X1=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0
       X2=(SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I+1))/2.0
       IF(X1.NE.X2)CALL DBL_IGRRECTANGLE(X1,YMIN,X2,SERIE(IIDF)%Y(I))
      ENDIF
     ENDIF
    ENDDO
    CALL UTL_DEBUGLEVEL(1)
   ENDIF
  ENDIF
  
  !## plot black lines on top of profile
  IF(ILINEBLACK.EQ.1)THEN
   CALL IGRCOLOURN(WRGB(0,0,0))
  ELSE
   CALL IGRCOLOURN(PROFIDF(IIDF)%SCOLOR)
  ENDIF

  !## lines
  IF(IPRF(2).EQ.1.OR.ILINEBLACK.EQ.1.AND. &
     IPRF(5).NE.1)THEN   !## cannot be used for idf files applied as colouring
   !## lines from centre
   IF(IBLOCKLINES.EQ.0)THEN
    LEX=.TRUE.
    DO I=1,SERIE(IIDF)%N
     IF(SERIE(IIDF)%Y(I).EQ.PROFIDF(IIDF)%IDF%NODATA)LEX=.TRUE.
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      IF(LEX)THEN
       CALL DBL_IGRMOVETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I)); LEX=.FALSE.
      ELSE
       CALL DBL_IGRLINETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I))
      ENDIF
     ENDIF
    ENDDO
   !## blocklines
   ELSE
    LEX=.TRUE.; J=0
    DO I=1,SERIE(IIDF)%N 
     IF(SERIE(IIDF)%Y(I).EQ.PROFIDF(IIDF)%IDF%NODATA)LEX=.TRUE.
     IF(SERIE(IIDF)%Y(I).NE.PROFIDF(IIDF)%IDF%NODATA)THEN
      J=J+1
      IF(LEX)THEN
       CALL DBL_IGRMOVETO(SERIE(IIDF)%X(I),SERIE(IIDF)%Y(I)); LEX=.FALSE.
      ELSE
       CALL DBL_IGRLINETO((SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0,SERIE(IIDF)%Y(I-1))
       CALL DBL_IGRLINETO((SERIE(IIDF)%X(I)+SERIE(IIDF)%X(I-1))/2.0,SERIE(IIDF)%Y(I))
      ENDIF
     ELSE
      IF(J.GT.1)CALL DBL_IGRLINETO(SERIE(IIDF)%X(I-1),SERIE(IIDF)%Y(I-1)); J=0
     ENDIF
    ENDDO
   ENDIF
  ENDIF
  
 ENDDO

 CALL IGRLINEWIDTH(1)
 
 END SUBROUTINE PROFILE_BITMAPIDF

