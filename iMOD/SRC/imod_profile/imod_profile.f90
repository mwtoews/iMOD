 CALL WDIALOGGETDOUBLE(IDF_REAL10,XSCALE)
 IF(XSCALE.LE.0.0D0)THEN; XSCALE=1.0D0; CALL WDIALOGPUTDOUBLE(IDF_REAL10,XSCALE); ENDIF
 
