 !###======================================================================
 IMPLICIT NONE

 !## profile from IDF's is determined
 IF(YMIN.LT.YMAX)RETURN

 YMIN=-50.0D0
 YMAX= 50.0D0

 END SUBROUTINE PROFILE_IPFMINMAX

