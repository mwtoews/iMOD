 IF(XMAX.LE.XMIN)XMAX=XMIN+1.0D0
 IF(YMAX.LE.YMIN)THEN
  YMAX=YMIN+5000.0D0
  YMIN=YMIN-5000.0D0
 ENDIF

 AXES%XMIN =XMIN
 AXES%XMAX =XMAX
 AXES%YMIN =YMIN
 AXES%YMAX =YMAX

 IF(ISCALE.EQ.1)THEN
  !## do something with scales - adjust y
  DX=       AXES%XMAX-AXES%XMIN
  Y =0.5D0*(AXES%YMAX+AXES%YMIN)
  DY=DX/XSCALE
  AXES%YMAX=Y+DY
  AXES%YMIN=Y-DY
 ENDIF
 
 AXES%XINT =XINT
 AXES%YINT =YINT
 AXES%ICLRRASTER=ICLRRASTER
 AXES%XFACTOR=1.0D0 
 AXES%YFACTOR=1.0D0
 !## 1.0D0/factor is ratio of screen to be used for margins
 AXES%DXAXESL=40.0D0  !## left
 AXES%DYAXESB=20.0D0 !/REAL(NGRAPH)  !## bottom
 AXES%DYAXEST=75.0D0  !## top
 AXES%DXAXESR=150.0D0 !## right
 AXES%TFONT=FFHELVETICA   !## text-font
 AXES%IAXES=(/1,0/)   !## left/bottom axes only
 AXES%XTITLE='Profile Distance (meters)'
 AXES%YTITLE=''
 AXES%LDATE=.FALSE.

 CALL WDIALOGSELECT(ID_DSERIESPROPTAB3)
 IF(AXES%IFIXX.EQ.0)THEN
  CALL WDIALOGPUTDOUBLE(IDF_REAL7,XMIN)
  CALL WDIALOGPUTDOUBLE(IDF_REAL8,XMAX)
  CALL WDIALOGPUTDOUBLE(IDF_REAL6,AXES%XINT)
 ENDIF
 IF(AXES%IFIXY.EQ.0)THEN
  CALL WDIALOGPUTDOUBLE(IDF_REAL3,YMIN)
  CALL WDIALOGPUTDOUBLE(IDF_REAL4,YMAX)
  CALL WDIALOGPUTDOUBLE(IDF_REAL5,AXES%YINT)
 ENDIF

