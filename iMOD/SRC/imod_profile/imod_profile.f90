 IF(PRESENT(LPS))THEN
  CALL PROFILE_PLOTSURVEY(LPS)
 ELSE
  CALL PROFILE_PLOTSURVEY(.FALSE.)
 ENDIF
 
 IF(ISOLID.NE.0)THEN
  !## plot extend of masks
  CALL SOLID_PROFILEDRAW_MASK()  
  !## plot extend of polygon(s)
  CALL SOLID_PROFILEDRAW_POLYGON()  
 ENDIF

 IF(PRESENT(LPS))THEN
  IF(LPS)RETURN
 ENDIF
 
 !## draw all bitmaps
 DO IWINID=1,SIZE(IWINPROFILE)
  !## putbitmap on entire screen or ratio
  CALL IGRSELECT(DRAWWIN,IWINPROFILE(IWINID))
  CALL WBITMAPPUT(PRF_IBITMAP(IWINID),0,1)   
 ENDDO
 
 END SUBROUTINE PROFILE_PLOT

 !###======================================================================
 SUBROUTINE PROFILE_PLOT_2DIDF(LPS)
 !###======================================================================
 IMPLICIT NONE
 LOGICAL,INTENT(IN) :: LPS
 INTEGER :: I,J,IPLOT,II
 REAL(KIND=DP_KIND) :: XMIN_ORG,YMIN_ORG,XMAX_ORG,YMAX_ORG,XA1,YA1,XA2,YA2,RD,DX,DY

 !## split window?
 
 !## plot profile with ipf's
    !## draw line of the profile... !##Liduin
   CALL IGRCOLOURN(WRGB(0,0,0))
   IF(.NOT.LPS)THEN
    DO J=2,NXY
