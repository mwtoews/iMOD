 !###======================================================================
 SUBROUTINE PROFILE_PROP_STATES(IROW)
 !###======================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: IROW
 INTEGER :: IP1,IP2,I,J,ICOL

 RETURN
 
 IF(IROW.LE.1.OR.IROW.GE.MXNIDF)RETURN

 IP1=0
 IP2=0
 IF(IROW.GT.2)       CALL WGRIDGETCELLCHECKBOX(IDF_GRID1,8,IROW-2,IP1)
