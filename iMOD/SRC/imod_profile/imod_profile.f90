  CALL WGRIDPUTCELLCHECKBOX(IDF_GRID1,8,IROW,0)
 ELSE
  CALL WGRIDGETCELLCHECKBOX(IDF_GRID1,8,IROW,I)
  I=I+1
  DO J=IROW-1,IROW+1
