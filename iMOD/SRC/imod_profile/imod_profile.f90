 SUBROUTINE PROFILE_TRANSFORMXY()
 !###======================================================================
 IMPLICIT NONE
 REAL(KIND=DP_KIND) :: XMIDDLE,YMIDDLE,DX,DY
 INTEGER :: I

 XMIDDLE=SUM(XY(1,1:NXY))/REAL(NXY)
 YMIDDLE=SUM(XY(2,1:NXY))/REAL(NXY)
 DO I=1,NXY
  DX=XY(1,I)-XMIDDLE
  DY=XY(2,I)-YMIDDLE
  XY(1,I)=XMIDDLE-1.0D0*DY
  XY(2,I)=YMIDDLE+1.0D0*DX
 END DO

 END SUBROUTINE PROFILE_TRANSFORMXY

