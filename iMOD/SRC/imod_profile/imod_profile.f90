    IF(NXY.GT.0.AND.IP.EQ.0)THEN 
     IF(WINFOMOUSE(MOUSECURSOR).EQ.ID_CURSORMOVE.OR. &
        WINFOMOUSE(MOUSECURSOR).EQ.ID_CURSORADJUSTPOINT)THEN

      LMOVEPROF=.TRUE.
      PROFX    =MOUSEX 
      PROFY    =MOUSEY 

     ENDIF
    ENDIF
