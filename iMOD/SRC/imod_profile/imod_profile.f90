     CALL PROFILE_PLOTVIEWBOX(XY(1,J-1),XY(2,J-1),XY(1,J),XY(2,J))
    END DO
   ENDIF
 
 !## plot selected idf's
 IF(NPIPET.EQ.0)RETURN 
 !## do not refresh if profile is moving
 IF(LMOVEPROF)RETURN

 CALL UTL_MESSAGEHANDLE(0)

 CALL WINDOWSELECT(0)

 DO II=1,SIZE(PRF_IBITMAP)
 
  CALL IGRSELECT(DRAWBITMAP,PRF_IBITMAP(II))
  CALL IGRPLOTMODE(MODECOPY)

  RD=REAL(WINFODRAWABLE(DRAWABLEWIDTH))/REAL(WINFODRAWABLE(DRAWABLEHEIGHT))

  DO I=1,SIZE(IPIPET)
   IPLOT=IPIPET(I)

   XA1=GRAPHAREA(3,1)
   YA1=REAL(I-1)*1.0D0/REAL(SIZE(IPIPET))
   XA2=1.0D0
   YA2=REAL(I)*1.0D0/REAL(SIZE(IPIPET))
   CALL DBL_IGRAREA(XA1,YA1,XA2,YA2)
   CALL IGRAREACLEAR()

   !## copy of original mpw%-coordinates
   XMIN_ORG=MPW%XMIN
   YMIN_ORG=MPW%YMIN
   XMAX_ORG=MPW%XMAX
   YMAX_ORG=MPW%YMAX

   MPW%XMIN=MINVAL(XY(1,1:NXY))
