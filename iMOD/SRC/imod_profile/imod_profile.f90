 !###======================================================================
 SUBROUTINE PROFILE_SNAPCOORDINATES(MOUSEX,MOUSEY)
 !###======================================================================
 IMPLICIT NONE
 REAL(KIND=DP_KIND),INTENT(INOUT) :: MOUSEX,MOUSEY
 INTEGER :: IIPF,IROW,I,J
 REAL(KIND=DP_KIND) :: X,Y,D,TD

 IF(NIPF.EQ.0)RETURN
 IF(ISNAP.EQ.0)RETURN

 TD=10.0D10; I=0; J=0
 
 DO IIPF=1,ABS(NIPF)

  !## snap to these points
  IF(MP(MPLOT(IIPF))%PRFTYPE.EQ.1)THEN

   DO IROW=1,IPF(IIPF)%NROW
    X=IPF(IIPF)%XYZ(1,IROW)
    Y=IPF(IIPF)%XYZ(2,IROW)
    IF(X.GE.MPW%XMIN.AND.X.LE.MPW%XMAX.AND.Y.GE.MPW%YMIN.AND.Y.LE.MPW%YMAX)THEN
     D=UTL_DIST(X,Y,MOUSEX,MOUSEY) !(X-MOUSEX)**2.0D0+(Y-MOUSEY)**2.0D0
!     IF(D.NE.0.0D0)D=SQRT(D)

     IF(D.LT.TD)THEN
      TD=D
      I =IIPF
      J =IROW
     ENDIF

    ENDIF
   END DO
  ENDIF
 END DO
 
 IF(I.NE.0)THEN
  MOUSEX=IPF(I)%XYZ(1,J)
  MOUSEY=IPF(I)%XYZ(2,J)
 ENDIF
 
 END SUBROUTINE PROFILE_SNAPCOORDINATES

