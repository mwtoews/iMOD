  CALL DBL_IGRUNITSFROMPIXELS(PBITMAP%IX1,PBITMAP%IY1,BMPX1,BMPY1,IORIGIN=1)
  CALL DBL_IGRUNITSFROMPIXELS(PBITMAP%IX2,PBITMAP%IY2,BMPX2,BMPY2,IORIGIN=1)
 ENDIF

 CALL PROFILE_COMPUTEPLOT()
 
 !## recompute min/max only in case of axes fixation
 IF(IRESETZOOM.EQ.1)CALL PROFILE_IDFMINMAX()
 
 CALL PROFILE_CLEAR()
 !## het number of screens
 CALL PROFILE_PLOT_NSCREEN()
 !## recreate windows --- if necessary (number of windows different than before)
 IF(NSCREEN.NE.SIZE(IWINPROFILE))CALL PROFILE_CREATEWINDOWS()
 !## redraw cross-section
 CALL PROFILE_PLOT()  
 
 !## replot to make sure adjusted coordinate affect bitmap
 IF(PBITMAP%IACT.NE.0)THEN
  CALL PROFILE_EXTENT_GRAPH(1)
  CALL DBL_IGRUNITSTOPIXELS(BMPX1,BMPY1,PBITMAP%IX1,PBITMAP%IY1,IORIGIN=1)
  CALL DBL_IGRUNITSTOPIXELS(BMPX2,BMPY2,PBITMAP%IX2,PBITMAP%IY2,IORIGIN=1)
  CALL PROFILE_PLOT()
 ENDIF
 
 !## adjust menu-fields
 CALL PROFILE_FIELDTOOLBAR(0,1)

