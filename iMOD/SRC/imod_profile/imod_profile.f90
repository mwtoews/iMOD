 !###======================================================================
 IMPLICIT NONE
 INTEGER,INTENT(IN) :: JD
 INTEGER,INTENT(INOUT) :: ICLRIROW
 
 IF(JD.EQ.ID_SELECT)THEN
  IF(ICLRIROW.NE.0)CALL WGRIDCOLOURCELL(IDF_GRID1,1,ICLRIROW,-1,-1)
  ICLRIROW=0
 ENDIF
 
 END SUBROUTINE PROFILE_PIPET_SEARCH_CLEAR
 
