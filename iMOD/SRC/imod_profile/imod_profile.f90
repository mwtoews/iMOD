 SUBROUTINE PROFILE_BITMAPIFF()
 !###======================================================================
 IMPLICIT NONE
 INTEGER :: I,J
 REAL(KIND=DP_KIND) :: ZMIN,ZMAX,DX,DXY
 REAL(KIND=DP_KIND) :: XMN,YMN,XMX,YMX
 REAL(KIND=DP_KIND),DIMENSION(4,2) :: XYPOL

 IF(MXNIFF.LE.0)RETURN

 CALL IGRLINETYPE(SOLIDLINE)
 CALL IGRFILLPATTERN(SOLID)
