   ENDIF
  ENDDO
  IF(I.EQ.2)THEN
   CALL WGRIDSTATECELL(IDF_GRID1,8,IROW,1)
  ENDIF
  CALL WGRIDSTATECELL(IDF_GRID1,8,1,2)
  CALL WGRIDSTATECELL(IDF_GRID1,8,MXNIDF,2)
 ENDIF

