 YINT=1.0D0
 IF(AXES%IFIXY.EQ.1)THEN
  CALL WDIALOGGETDOUBLE(IDF_REAL3,YMIN)
  CALL WDIALOGGETDOUBLE(IDF_REAL4,YMAX)
  CALL WDIALOGGETDOUBLE(IDF_REAL5,YINT)
 ENDIF
 
