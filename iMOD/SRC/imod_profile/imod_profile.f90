 SUBROUTINE PROFILE_PLOTCOORDINATES()
 !###======================================================================
 IMPLICIT NONE
 REAL(KIND=DP_KIND) :: X1,X2,Y1,Y2,DX,DXS,YY1
 INTEGER :: I,J
 CHARACTER(LEN=50) :: STRING
 
 !## no coordinates available
 IF(NXY.EQ.0)RETURN
 
 !## get current graph-dimensions
 X1 =INFOGRAPHICS(GRAPHICSUNITMINX); X2 =INFOGRAPHICS(GRAPHICSUNITMAXX)
 Y1 =INFOGRAPHICS(GRAPHICSUNITMINY); Y2 =INFOGRAPHICS(GRAPHICSUNITMAXY)
 CALL DBL_WGRTEXTFONT(IFAMILY=AXES%TFONT,TWIDTH=AXES%CHW,THEIGHT=AXES%CHH,ISTYLE=0)
 !## plot axes-text
 CALL IGRCOLOURN(WRGB(0,0,0))

 CALL DBL_WGRTEXTORIENTATION(ALIGNLEFT,ANGLE=00.0D0)

 CALL WDIALOGSELECT(ID_DSERIESPROPTAB3)
 CALL WDIALOGGETCHECKBOX(IDF_CHECK9,I)
 IF(I.EQ.1)THEN
  CALL WDIALOGGETDOUBLE(IDF_REAL10,DX)
  YY1=Y1+((Y2-Y1)/50.0D0)
  CALL DBL_WGRTEXTSTRING(X1,YY1,' Scale 1:'//TRIM(RTOS(DX,'F',2))) 
 ENDIF
 
 !## plotting selected!
 CALL WDIALOGSELECT(ID_DSERIESPROPTAB2)
 CALL WDIALOGGETCHECKBOX(IDF_CHECK1,I)
 CALL WDIALOGGETCHECKBOX(IDF_CHECK2,J)
 IF(I.EQ.0)RETURN

 DX=0.0D0
 
 CALL DBL_WGRTEXTORIENTATION(ALIGNLEFT,ANGLE=90.0D0)

 !## colors the label text and label background at given x,y coordinate
 CALL IGRCOLOURN(WRGB(255,255,255))
 CALL DBL_WGRTEXTFONT(IFAMILY=AXES%TFONT,TWIDTH=AXES%CHW,THEIGHT=AXES%CHH,ISTYLE=FSOPAQUE)
 CALL IGRCOLOURN(WRGB(0,0,0))
 CALL DBL_WGRTEXTFONT(IFAMILY=AXES%TFONT,TWIDTH=AXES%CHW,THEIGHT=AXES%CHH)

 YY1=Y1+((Y2-Y1)/50.0D0)
 DO I=1,NXY
  
  STRING='['//TRIM(ITOS(INT(XY(1,I))))//'m,'//TRIM(ITOS(INT(XY(2,I))))//'m,'//TRIM(XYLABEL(I))//']'
  DXS=WGRTEXTLENGTH('0'//TRIM(STRING)//'0',0)*AXES%CHW 

  IF(I.GT.1)THEN
   DX=DX+SQRT((XY(1,I)-XY(1,I-1))**2.0D0+(XY(2,I)-XY(2,I-1))**2.0D0)
  ENDIF 
  
  IF(J.EQ.1.AND.TRIM(XYLABEL(I)).EQ.'')CYCLE

  CALL DBL_WGRTEXTSTRING(DX,YY1,' '//TRIM(STRING)//' ') 
    
 ENDDO

 CALL IGRFILLPATTERN(OUTLINE)

 END SUBROUTINE PROFILE_PLOTCOORDINATES

