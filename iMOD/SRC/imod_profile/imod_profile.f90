 !###====================================================================
 SUBROUTINE PROFILE_MOUSEMOVE()
 !###====================================================================
 IMPLICIT NONE
 INTEGER :: I,IWINID
 REAL(KIND=DP_KIND) :: CRITDIST,CRITPROF,DXY,X1,Y1,MOUSEX,MOUSEY

 CRITDIST=SQRT((MPW%XMAX-MPW%XMIN)**2.0D0+(MPW%YMAX-MPW%YMIN)**2.0D0)/100.0D0
 CRITPROF=0.0D0

 !## message from 2d-plot window
 IF(MESSAGE%WIN.EQ.ID_DSERIESTAB1)THEN

  CALL DBL_IGRAREA(AREA(1),AREA(2),AREA(3),AREA(4))
  CALL DBL_IGRUNITS(MPW%XMIN,MPW%YMIN,MPW%XMAX,MPW%YMAX,IOFFSET=1)

  !## shift mouse coordinates
  MOUSEX=DBLE(MESSAGE%GX)+OFFSETX
  MOUSEY=DBLE(MESSAGE%GY)+OFFSETY

  CALL WINDOWSELECT(0)
  CALL WINDOWOUTSTATUSBAR(1,'X: '//TRIM(RTOS(MOUSEX,'F',3))//' m, Y: '//TRIM(RTOS(MOUSEY,'F',3))//' m')

  IF(IP.EQ.0.AND..NOT.LMOVEPROF)THEN
   DXY=(SQRT((MPW%XMAX-MPW%XMIN)**2.0D0+(MPW%YMAX-MPW%YMIN)**2.0D0))/100.0D0
   !## check box
   IXY=0
   DO I=1,NXY
    IF(MOUSEX.GT.XY(1,I)-DXY.AND.MOUSEX.LT.XY(1,I)+DXY.AND. &
       MOUSEY.GT.XY(2,I)-DXY.AND.MOUSEY.LT.XY(2,I)+DXY)EXIT
   END DO
   IF(I.GT.NXY)THEN
    DO I=2,NXY
     IF(DBL_IGRDISTANCELINE(XY(1,I-1),XY(2,I-1),XY(1,I),XY(2,I),MOUSEX,MOUSEY,0).LE.CRITDIST)EXIT
    END DO
    IF(I.LE.NXY)THEN
     IF(WINFOMOUSE(MOUSECURSOR).NE.ID_CURSORMOVE)CALL WCURSORSHAPE(ID_CURSORMOVE)
    ELSE
     IF(WINFOMOUSE(MOUSECURSOR).NE.CURARROW)CALL WCURSORSHAPE(CURARROW)
    ENDIF
   ELSE
    IXY=I
    IF(WINFOMOUSE(MOUSECURSOR).NE.ID_CURSORADJUSTPOINT)CALL WCURSORSHAPE(ID_CURSORADJUSTPOINT)
   ENDIF
  ENDIF

  !## move current drawn profile
  IF(LMOVEPROF)THEN
   CALL PROFILE_CLEAR()
   !## move entire line(s)
   IF(IXY.EQ.0)THEN
    XY(1,1:NXY)=XY(1,1:NXY)+(MOUSEX-PROFX)
    XY(2,1:NXY)=XY(2,1:NXY)+(MOUSEY-PROFY)
   !## move current point
   ELSE
    !## snap new point ... yes-or-no
    CALL PROFILE_SNAPCOORDINATES(MOUSEX,MOUSEY)
    XY(1,IXY)=XY(1,IXY)+(MOUSEX-PROFX)
    XY(2,IXY)=XY(2,IXY)+(MOUSEY-PROFY)
   ENDIF
   PROFX=MOUSEX
   PROFY=MOUSEY
   CALL PROFILE_COMPUTEPLOT()
   CALL PROFILE_IDFMINMAX()
   CALL PROFILE_PLOT()
   XPOSPROF=0.0D0
   CALL PROFILE_CLEAR()
  ENDIF

  !## first point set!
  IF(IDOWN_PRF.EQ.1)THEN

   CALL WCURSORSHAPE(ID_CURSORPROFILE)

   !## update profile-line - within bitmap
   CALL PROFILE_EXTENT_2DBITMAP()

   CALL IGRLINEWIDTH(LINEWIDTHPLOT)
   CALL IGRCOLOURN(LINECOLORPLOT)

   IF(LLINE)THEN
    CALL DBL_IGRJOIN(XY(1,NXY),XY(2,NXY),XCRD,YCRD,IOFFSET=1)
    !## removing optional plot viewing 'window' for particle/point plotting
    CALL PROFILE_PLOTVIEWBOX(XY(1,NXY),XY(2,NXY),XCRD,YCRD)
    CALL IGRLINEWIDTH(LINEWIDTHPLOT)
    CALL IGRCOLOURN(LINECOLORPLOT)
   ENDIF
   !## plot profile line and optional viewing 'window' for particle/point plotting
   LLINE=.TRUE.

   !## snap coordinates ... yes-or-no
   CALL PROFILE_SNAPCOORDINATES(MOUSEX,MOUSEY)

   X1   =MOUSEX 
   Y1   =MOUSEY 
   CALL DBL_IGRJOIN(XY(1,NXY),XY(2,NXY),X1,Y1,IOFFSET=1)
   CALL PROFILE_PLOTVIEWBOX(XY(1,NXY),XY(2,NXY),X1,Y1)
   CALL IGRLINEWIDTH(1)

   IF(ABS(XCRD-MOUSEX).GT.CRITPROF.OR.ABS(YCRD-MOUSEY).GT.CRITPROF)THEN

    XCRD=MOUSEX 
    YCRD=MOUSEY 

    !## temporary increase number of points, especially for iff/ipf plotting purposes
    NXY=NXY+1; CALL PROFILE_WTIADDPOINT_MEMORY(NXY)
    XY(1,NXY)=MOUSEX 
    XY(2,NXY)=MOUSEY 
    CALL PROFILE_COMPUTEPLOT()
    CALL PROFILE_IDFMINMAX()
    CALL PROFILE_PLOT()
    NXY=NXY-1

    !## position 2d-plot
    CALL PROFILE_PUTBITMAP(MPW%IBITMAP) 

   ELSE
    CALL DBL_IGRAREA(AREA(1),AREA(2),AREA(3),AREA(4))
    CALL DBL_IGRUNITS(MPW%XMIN,MPW%YMIN,MPW%XMAX,MPW%YMAX,IOFFSET=1)
   ENDIF
  ENDIF

 !## graphical window for the cross-section 
 ELSE

  !## find window
  DO I=1,SIZE(IWINPROFILE); IF(MESSAGE%WIN.EQ.IWINPROFILE(I))EXIT; ENDDO
  IF(I.LE.SIZE(IWINPROFILE))THEN
   !## update all windows
   DO IWINID=1,SIZE(IWINPROFILE)
    !## activate current window   
    CALL PROFILE_EXTENT_GRAPH(IWINID)   
    CALL WINDOWOUTSTATUSBAR(1,'Distance: '//TRIM(RTOS(REAL(MESSAGE%GX,8),'F',3))//' m, Map Value: '//TRIM(RTOS(REAL(MESSAGE%GY,8),'F',3))) 
   ENDDO
   !## solid adjustment?
   IF(NXY.GT.0)THEN
    IF(IDOWN_SOLID.EQ.0)THEN 
     CALL SOLID_PROFILEMOUSE(REAL(MESSAGE%GX,8),REAL(MESSAGE%GY,8),ICURSOR_SOLID,ICRD_SOLID,IELEV_SOLID,I) 
     IF(ICURSOR_SOLID.EQ.0)THEN
      CALL PROFILE_BACKGROUND_BITMAP_MOUSEMOVE(MESSAGE%XPIX,MESSAGE%YPIX,ICURSOR_BITMAP,ICRD_BITMAP)
      !## selected bitmap
      IF(ICURSOR_BITMAP.NE.0)THEN
       ICURSOR_SOLID=0; ICRD_SOLID=0; IELEV_SOLID=0
      ELSE
       !## try to select survey plot
       CALL PROFILE_PLOTSURVEY_MOUSEMOVE(REAL(MESSAGE%XPIX,8),REAL(MESSAGE%YPIX,8),ICURSOR_SURVEY,ICRD_SURVEY)
       IF(ICURSOR_SURVEY.NE.0)THEN
        ICURSOR_SOLID=0; ICRD_SOLID=0; IELEV_SOLID=0
       ENDIF
      ENDIF
     ELSE
      ICURSOR_BITMAP=0; ICRD_BITMAP=0
     ENDIF
    ELSE
     IF(IELEV_SOLID.NE.0)THEN
      !## remove previous selected line ... draw profile to be editable only
      DO IWINID=1,SIZE(IWINPROFILE)
       CALL SOLID_PROFILEDRAW(IELEV_SOLID,IELEV_SOLID,ICRD_SOLID,ICURSOR_SOLID,IWINID)
      ENDDO
      CALL SOLID_PROFILEADJUST(REAL(MESSAGE%GX,8),REAL(MESSAGE%GY,8),ICURSOR_SOLID,ICRD_SOLID,IELEV_SOLID,I) 
      !## draw new profile to be editable only
      DO IWINID=1,SIZE(IWINPROFILE)
       CALL SOLID_PROFILEDRAW(IELEV_SOLID,IELEV_SOLID,ICRD_SOLID,ICURSOR_SOLID,IWINID) 
       !## putbitmap on entire screen or ratio
       CALL IGRSELECT(DRAWWIN,IWINPROFILE(IWINID))
       CALL WBITMAPPUT(PRF_IBITMAP(IWINID),0,1)
      ENDDO
     ENDIF
     IF(ICURSOR_BITMAP.NE.0)THEN
      CALL PROFILE_BACKGROUND_BITMAP_ADJUST(MESSAGE%XPIX,MESSAGE%YPIX,ICURSOR_BITMAP) 
      CALL PROFILE_PLOT()
     ENDIF
     IF(ICURSOR_SURVEY.NE.0)THEN
      CALL PROFILE_PLOTSURVEY_ADJUST(REAL(MESSAGE%XPIX,8),REAL(MESSAGE%YPIX,8),ICURSOR_SURVEY) 
      CALL PROFILE_PLOT()
     ENDIF
    ENDIF
   ENDIF
   !## view on 2d-plot old position of cross-section
   CALL PROFILE_CLEAR(); XPOSPROF=DBLE(MESSAGE%GX); CALL PROFILE_CLEAR()
   CALL PROFILE_EXTENT_GRAPH(I)   
  ENDIF
   
 ENDIF

 END SUBROUTINE PROFILE_MOUSEMOVE

